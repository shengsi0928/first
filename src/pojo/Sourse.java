package pojo;

public class Sourse {
	private String fruit;
	private String sugar;
	private String size;
	
	public String getFruit(){
		return fruit;
	}
	public String getSugar(){
		return sugar;
	}
	public String getSize(){
		return size;
	}
	
	public String setFruit(String fruit){
		this.fruit = fruit;
		return fruit;
	}
	public String setSugar(String sugar){
		this.sugar = sugar;
		return sugar;
	}
	public String setSize(String size){
		this.size = size;
		return size;
	}
}
